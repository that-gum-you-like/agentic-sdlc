#!/usr/bin/env node
/**
 * Browser User Test — autonomous Playwright user-testing with screenshots
 * (curriculum Phase 7), built on the Tier-5 patterns in
 * agents/templates/browser-tests.md.template.
 *
 * Two commands:
 *
 *   generate <change-name>   Build a Playwright user-journey spec FROM an
 *                            OpenSpec change: every user-facing scenario /
 *                            user-story line in the proposal becomes a test
 *                            with a per-step screenshot discipline baked in.
 *                            Deterministic scaffold by default; --llm fills
 *                            in step implementations via the OpenRouter
 *                            adapter (Hermes ladder — never OpenAI).
 *
 *   run [<spec-path>]        Execute the project's OWN Playwright against the
 *                            generated specs, archiving screenshots under
 *                            pm/user-tests/<date>/. Skips gracefully (exit 0,
 *                            clear message) when Playwright isn't installed —
 *                            the framework itself stays zero-dep.
 *
 * Usage:
 *   node agents/browser-user-test.mjs generate <change-name> [--llm] [--dry-run]
 *   node agents/browser-user-test.mjs run [spec] [--dry-run]
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { resolve, dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { execFileSync } from 'child_process';

import { loadConfig } from './load-config.mjs';
import { logCapabilityUsage } from './capability-logger.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// ---------------------------------------------------------------------------
// Scenario extraction from OpenSpec artifacts (deterministic)
// ---------------------------------------------------------------------------

/**
 * Pull user-facing scenarios out of OpenSpec markdown: `#### Scenario:` blocks,
 * `As a … I want …` user-story lines, and `Demo Sentence:` lines.
 * @returns {Array<{title: string, steps: string[]}>}
 */
export function extractScenarios(markdown) {
  const scenarios = [];
  const lines = String(markdown || '').split('\n');

  let current = null;
  for (const line of lines) {
    const scenarioHeading = line.match(/^#{3,4}\s*Scenario:\s*(.+)$/i);
    if (scenarioHeading) {
      if (current) scenarios.push(current);
      current = { title: scenarioHeading[1].trim(), steps: [] };
      continue;
    }
    if (current) {
      const step = line.match(/^\s*-\s*\*\*(WHEN|THEN|AND|GIVEN)\*\*\s*(.+)$/i)
        || line.match(/^\s*-\s*(WHEN|THEN|AND|GIVEN)\b\s*(.+)$/i);
      if (step) {
        current.steps.push(`${step[1].toUpperCase()} ${step[2].trim()}`);
        continue;
      }
      if (/^#{1,4}\s/.test(line)) { // next section ends the scenario
        scenarios.push(current);
        current = null;
      }
    }

    // A user-story / demo line is its own journey. Close any open Scenario
    // block first so a story trailing the last scenario is still captured
    // (the `!current` guard silently dropped it before).
    const userStory = line.match(/^\s*[-*]?\s*As an?\s+(.+?),?\s+I (?:want|can|need)\s+(.+?)(?:\s+so that.*)?$/i);
    if (userStory) {
      if (current) { scenarios.push(current); current = null; }
      scenarios.push({ title: `As a ${userStory[1].trim()}: ${userStory[2].trim()}`, steps: [] });
      continue;
    }
    const demo = line.match(/Demo Sentence:\s*(.+)$/i);
    if (demo) {
      if (current) { scenarios.push(current); current = null; }
      scenarios.push({ title: demo[1].trim(), steps: [] });
    }
  }
  if (current) scenarios.push(current);
  return scenarios;
}

// ---------------------------------------------------------------------------
// Spec generation (per-step screenshot discipline from the Tier-5 template)
// ---------------------------------------------------------------------------

function slugify(text) {
  return String(text).toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '').slice(0, 60);
}

/**
 * Build the Playwright spec source for a change's scenarios.
 * Each test screenshots EVERY step (the template's "screenshots as proof"
 * principle) into the run's archive dir via the SCREENSHOT_DIR env var.
 */
export function buildSpecSource(changeName, scenarios) {
  const header = `// Auto-generated by agents/browser-user-test.mjs from openspec/changes/${changeName}
// Per-step screenshots are the Tier-5 proof discipline (browser-tests.md.template).
// Regenerate: node agents/browser-user-test.mjs generate ${changeName}
import { test, expect } from '@playwright/test';
import path from 'path';

const SHOTS = process.env.SCREENSHOT_DIR || 'pm/user-tests/latest';
let shot = 0;
async function proof(page, label) {
  shot += 1;
  await page.screenshot({ path: path.join(SHOTS, \`\${String(shot).padStart(2, '0')}-\${label}.png\`), fullPage: true });
}
`;

  const tests = scenarios.map((s) => {
    const stepLines = (s.steps.length > 0 ? s.steps : ['WHEN the user performs the journey', 'THEN the expected result is visible'])
      .map((step) => {
        const label = slugify(step);
        return `    // ${step}
    // TODO(user-test): implement this step against the app (navigate/click/fill/expect)
    await proof(page, '${label}');`;
      }).join('\n\n');

    return `
test(${JSON.stringify(s.title)}, async ({ page }) => {
  await page.goto('/');
  await proof(page, 'entry-${slugify(s.title)}');

${stepLines}

  // Final state must be a real screen, never blank (navigation-completeness pattern)
  await expect(page.locator('body')).not.toBeEmpty();
  await proof(page, 'final-${slugify(s.title)}');
});
`;
  }).join('\n');

  return header + tests;
}

// ---------------------------------------------------------------------------
// Optional LLM step fill-in (OpenRouter / Hermes ladder — never OpenAI)
// ---------------------------------------------------------------------------

async function llmFillSteps(specSource, changeName, config) {
  const { loadLlmAdapter } = await import('./adapters/load-adapter.mjs');
  const adapter = await loadLlmAdapter(config, 'openrouter');
  const res = await adapter.complete(
    `You are the QA engineer for this project. Below is a generated Playwright user-test spec with TODO steps extracted from the OpenSpec change "${changeName}". Replace each TODO with real Playwright actions (goto/click/fill/expect) matching the step comment above it. Keep the proof() screenshot calls exactly where they are. Return ONLY the complete TypeScript file, no prose.\n\n${specSource}`,
    { maxTokens: 4000, temperature: 0 },
  );
  const code = res.text.replace(/^```[a-z]*\n?/m, '').replace(/\n?```\s*$/m, '');
  // Never accept an LLM rewrite that drops the screenshot discipline.
  if (!code.includes('proof(') || !code.includes("from '@playwright/test'")) {
    console.warn('⚠️  LLM output dropped the screenshot discipline — keeping the deterministic scaffold');
    return specSource;
  }
  return code;
}

// ---------------------------------------------------------------------------
// Commands
// ---------------------------------------------------------------------------

async function cmdGenerate(changeName, { llm = false, dryRun = false } = {}) {
  const config = loadConfig();
  try { logCapabilityUsage('browserE2E', process.env.AGENT || 'system', changeName, 'browser-user-test.mjs', 'generate'); } catch { /* advisory */ }

  const changeDir = resolve(config.projectDir, 'openspec', 'changes', changeName);
  if (!existsSync(changeDir)) {
    console.error(`No such OpenSpec change: ${changeName} (${changeDir})`);
    process.exit(1);
  }

  let markdown = '';
  for (const artifact of ['proposal.md', 'design.md', 'tasks.md']) {
    const p = join(changeDir, artifact);
    if (existsSync(p)) markdown += readFileSync(p, 'utf8') + '\n';
  }
  // Specs live one level down
  const specsDir = join(changeDir, 'specs');
  if (existsSync(specsDir)) {
    for (const f of (await import('fs')).readdirSync(specsDir).filter(f => f.endsWith('.md'))) {
      markdown += readFileSync(join(specsDir, f), 'utf8') + '\n';
    }
  }

  const scenarios = extractScenarios(markdown);
  if (scenarios.length === 0) {
    console.log(`No user-facing scenarios found in ${changeName} — nothing to generate.`);
    return null;
  }

  let source = buildSpecSource(changeName, scenarios);
  if (llm) {
    try {
      source = await llmFillSteps(source, changeName, config);
    } catch (err) {
      console.warn(`⚠️  LLM fill-in unavailable (${err.message}) — keeping the deterministic scaffold`);
    }
  }

  const outDir = resolve(config.appPath, 'e2e', 'user-tests');
  const outPath = join(outDir, `${slugify(changeName)}.spec.ts`);
  if (dryRun) {
    console.log(`[dry-run] would write ${outPath} (${scenarios.length} scenario(s))`);
    console.log(source.split('\n').slice(0, 30).join('\n'));
    return outPath;
  }
  mkdirSync(outDir, { recursive: true });
  writeFileSync(outPath, source);
  console.log(`🧪 Generated ${outPath} — ${scenarios.length} user-journey test(s), per-step screenshots wired.`);
  console.log(`   Fill any TODO steps (or rerun with --llm), then: node agents/browser-user-test.mjs run`);
  return outPath;
}

function playwrightAvailable(appPath) {
  return existsSync(join(appPath, 'node_modules', '@playwright', 'test'))
    || existsSync(join(appPath, 'node_modules', 'playwright'));
}

function cmdRun(specPath, { dryRun = false } = {}) {
  const config = loadConfig();
  try { logCapabilityUsage('browserE2E', process.env.AGENT || 'system', specPath || 'all', 'browser-user-test.mjs', 'run'); } catch { /* advisory */ }

  if (!playwrightAvailable(config.appPath)) {
    console.log('🧪 Playwright not installed in the target app — user-test run skipped.');
    console.log(`   Install in ${config.appPath}: npm i -D @playwright/test && npx playwright install`);
    return { skipped: true };
  }

  const stamp = new Date().toISOString().replace(/[:.]/g, '-');
  const shotsDir = resolve(config.projectDir, 'pm', 'user-tests', stamp);
  mkdirSync(shotsDir, { recursive: true });

  const args = ['playwright', 'test'];
  if (specPath) args.push(specPath);
  else args.push('e2e/user-tests');

  if (dryRun) {
    console.log(`[dry-run] would run: npx ${args.join(' ')} (screenshots → ${shotsDir})`);
    return { skipped: false, dryRun: true };
  }

  console.log(`🧪 Running user tests (screenshots → ${shotsDir})`);
  try {
    const out = execFileSync('npx', args, {
      cwd: config.appPath,
      encoding: 'utf8',
      timeout: 900_000,
      env: { ...process.env, SCREENSHOT_DIR: shotsDir },
    });
    console.log(out.split('\n').slice(-15).join('\n'));
    console.log(`✅ User tests passed. Proof screenshots: ${shotsDir}`);
    return { skipped: false, passed: true, shotsDir };
  } catch (err) {
    console.error(String(err.stdout || err.message).split('\n').slice(-25).join('\n'));
    console.error(`❌ User tests failed. Screenshots (incl. failures): ${shotsDir}`);
    process.exit(1);
  }
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

const __isMainModule = process.argv[1] && resolve(process.argv[1]) === __filename;

if (__isMainModule) {
  const [,, cmd, ...args] = process.argv;
  const flags = { llm: args.includes('--llm'), dryRun: args.includes('--dry-run') };
  const positional = args.filter(a => !a.startsWith('--'));

  switch (cmd) {
    case 'generate':
      if (!positional[0]) { console.error('Usage: browser-user-test.mjs generate <change-name> [--llm] [--dry-run]'); process.exit(1); }
      await cmdGenerate(positional[0], flags);
      break;
    case 'run':
      cmdRun(positional[0], flags);
      break;
    default:
      console.log(`Usage:
  browser-user-test.mjs generate <change-name> [--llm] [--dry-run]  # OpenSpec change → Playwright user tests
  browser-user-test.mjs run [spec] [--dry-run]                       # Run with per-step screenshot archive`);
  }
}
